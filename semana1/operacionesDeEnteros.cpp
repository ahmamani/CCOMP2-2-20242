#include <iostream>

int main() {

    int var1 = 100;
    int var2 = 20;

    std::cout << var1 + var2 << std::endl; // 120
    std::cout << var1 - var2 << std::endl; // 80
    std::cout << var1 * var2 << std::endl; // 2000
    std::cout << var1 / var2 << std::endl; // 5
    std::cout << var1 % var2 << std::endl; // 0

    return 0;
}